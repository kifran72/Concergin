    <p>Basée à Fontainebleau, Coniergin est une entreprise de service de proximité qui offre aux salariés des entreprises et aux usagers de la gare de Fontainebleau & Avon l’occasion de résoudre les tracasseries du quotidien, et les demandes relevant de la sphère privée. L’essentiel de notre prestation réside dans un service alliant rapidité, proximité et responsabilité sociale.
Le modèle d’organisation d’Easyday est un «circuit court» de distribution, profitant à la fois aux salariés de l’arrondissement de Fontainebleau, aux prestataires et services locaux partageant les mêmes valeurs éthiques et citoyennes qu’Easyday.</p>
